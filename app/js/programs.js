// その日のタスク（フォーカス＋coreの全タスク文字列）
window.programTasks = function (id, day) {
  const p = window.YOBOU_PROGRAMS[id];
  const d = p.days[day - 1];
  return [d.t].concat(p.core);
};
// 進行中の日（1-30、全完了なら31）
window.programCurrentDay = function (id) {
  const st = window.Store.getProgramState(id);
  for (let n = 1; n <= 30; n++) {
    if ((st.days[n] || []).length < window.programTasks(id, n).length) return n;
  }
  return 31;
};
// 完了した日数
window.programDoneDays = function (id) {
  const st = window.Store.getProgramState(id);
  let n = 0;
  for (let d = 1; d <= 30; d++) if ((st.days[d] || []).length >= window.programTasks(id, d).length) n++;
  return n;
};
